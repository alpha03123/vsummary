"""思维导图生成相关的 LLM 提示词模板。

集中维护「根据视频结构化概况生成前端可交互思维导图」所需的提示词；
模板由同调用方使用 `str.format` 注入占位符。
"""

# 用途：根据单视频的总结 JSON 生成前端可交互的思维导图。
#
# 业务定位：思维导图强调"知识结构"而非"章节目录"；要求 LLM 在
# 视频时长约束下把总结重新组织为可点击、可折叠的层级树，便于
# 前端按节点跳转时间戳。
#
# 输入占位符：`{title}`（视频标题）、`{duration_seconds}`（视频时长秒数）、
# `{summary_json}`（结构化总结 JSON 字符串）、
# `{transcript_text}`（转写文本，已由调用方截断）。
#
# 输出形态：JSON 树，每个节点包含 `id` / `title` / `summary` /
# `start_seconds` / `end_seconds` / `children`；节点组织按"知识结构"而非
# "章节目录"，时间戳必须落在视频时长内。
MINDMAP_PROMPT_TEMPLATE = (
    "请基于以下视频概况信息，生成一个适合前端交互展示的思维导图 JSON。\n"
    "要求：\n"
    "1. 只输出 JSON，不要输出额外解释。\n"
    "2. 不要编造 summary 中不存在的信息。\n"
    "3. 导图节点必须是树结构，且每个节点都包含 id、title、summary、start_seconds、end_seconds、children。\n"
    "4. 请按知识结构组织节点，而不是机械复述章节目录；可以参考章节，但不要被时间顺序束缚。\n"
    "5. 层级深度由内容复杂度决定：简单主题可以较浅，复杂主题可以自然展开到更深层，但每一层都必须有信息价值。\n"
    "6. 节点标题尽量简洁，优先使用关键词或短语，不要把整句摘要直接当标题。\n"
    "7. 时间范围必须落在视频时长内。\n\n"
    "视频标题：{title}\n"
    "视频时长秒数：{duration_seconds}\n"
    "概况 JSON：\n"
    "{summary_json}\n"
    "转写文本：{transcript_text}\n"
)
